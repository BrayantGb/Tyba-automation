package co.com.Tyba.prueba.models;

public class NewUser {

    private String Username;
    private String Email;
    private String InvalidEmail;
    private String Password;
    private String Confirma_password;
    private String First_Name;
    private String Last_Name;
    private String Phone_Number;
    private String Country;
    private String City;
    private String Address;
    private String State;
    private String Postal_Code;
    private String ConfirmPasswordIncorrect;


    public String getUsername() {
        return Username;
    }

    public void setUsername(String username) {
        Username = username;
    }

    public String getEmail() {
        return Email;
    }

    public void setEmail(String email) {
        Email = email;
    }

    public String getInvalidEmail() {
        return InvalidEmail;
    }

    public void setInvalidEmail(String invalidEmail) {
        InvalidEmail = invalidEmail;
    }

    public String getPassword() {
        return Password;
    }

    public void setPassword(String password) {
        Password = password;
    }

    public String getConfirma_password() {
        return Confirma_password;
    }

    public void setConfirma_password(String confirma_password) {
        Confirma_password = confirma_password;
    }

    public String getFirst_Name() {
        return First_Name;
    }

    public void setFirst_Name(String first_Name) {
        First_Name = first_Name;
    }

    public String getLast_Name() {
        return Last_Name;
    }

    public void setLast_Name(String last_Name) {
        Last_Name = last_Name;
    }

    public String getPhone_Number() {
        return Phone_Number;
    }

    public void setPhone_Number(String phone_Number) {
        Phone_Number = phone_Number;
    }

    public String getCountry() {
        return Country;
    }

    public void setCountry(String country) {
        Country = country;
    }

    public String getCity() {
        return City;
    }

    public void setCity(String city) {
        City = city;
    }

    public String getAddress() {
        return Address;
    }

    public void setAddress(String address) {
        Address = address;
    }

    public String getState() {
        return State;
    }

    public void setState(String state) {
        State = state;
    }

    public String getPostal_Code() {
        return Postal_Code;
    }

    public void setPostal_Code(String postal_Code) {
        Postal_Code = postal_Code;
    }

    public String getConfirmPasswordIncorrect() {
        return ConfirmPasswordIncorrect;
    }

    public void setConfirmPasswordIncorrect(String confirmPasswordIncorrect) {
        ConfirmPasswordIncorrect = confirmPasswordIncorrect;
    }
}
